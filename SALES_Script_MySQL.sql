CREATE DATABASE SALES;
USE SALES;

CREATE TABLE CUSTOMERS(
 CUSTOMER_ID	INT	NOT NULL,
 COMPANY	CHAR(50),
 FIRST_NAME     CHAR(30) NOT NULL,
 LAST_NAME	CHAR(30) NOT NULL,
 ADDRESS1	CHAR(72)	NOT NULL,
 ADDRESS2	CHAR(72),
 CITY	CHAR(30)	NOT NULL,
 STATE	CHAR(2)	NOT NULL,
 POSTAL_CODE	CHAR(10)		NOT NULL,
 PHONE	CHAR(30)	NOT NULL,
 EMAIL	CHAR(30)
);

ALTER TABLE CUSTOMERS 
ADD PRIMARY KEY (CUSTOMER_ID);

CREATE TABLE DEPARTMENT (
 DEPTID		CHAR(5)	NOT NULL,
 DEPTNAME	CHAR(30)
);

ALTER TABLE DEPARTMENT 
ADD PRIMARY KEY (DEPTID);

CREATE TABLE DISTRIBUTORS (
 DISTRIBUTOR_ID	INT	NOT NULL,
 DISTRIBUTOR_NAME	CHAR(48)	NOT NULL,
 CONTACT_PERSON	CHAR(48),
 CONTACT_PHONE	CHAR(32),
 CONTACT_EMAIL	CHAR(72)
);

ALTER TABLE DISTRIBUTORS 
ADD PRIMARY KEY (DISTRIBUTOR_ID);

CREATE TABLE JOBS (
 JOB_ID	INT	NOT NULL,
 JOB_DESC	CHAR(72)	NOT NULL
);
ALTER TABLE JOBS
ADD PRIMARY KEY(JOB_ID);


CREATE TABLE MANUFACTURERS(
 MANUFACTURER_ID	INT	NOT NULL,
 MANUFACTURER_NAME	CHAR(48)	NOT NULL
);

ALTER TABLE MANUFACTURERS
ADD PRIMARY KEY(MANUFACTURER_ID);

CREATE TABLE EMPLOYEES (
 EMPLOYEE_ID	INT	NOT NULL,
 JOB_ID	INT	NOT NULL,
 FIRST_NAME	CHAR(30)	NOT NULL,
 LAST_NAME	CHAR(30)	NOT NULL,
 ADDRESS1	CHAR(72)	NOT NULL,
 ADDRESS2	CHAR(72),
 CITY	CHAR(32),
 STATE	CHAR(2),
 POSTAL_CODE	CHAR(10),
 HOME_PHONE	CHAR(30)	NOT NULL,
 OFFICE_PHONE	CHAR(30),
 EMAIL	CHAR(30)
);

ALTER TABLE EMPLOYEES 
ADD PRIMARY KEY (EMPLOYEE_ID);

ALTER TABLE EMPLOYEES
ADD FOREIGN KEY (JOB_ID) REFERENCES JOBS(JOB_ID);

CREATE TABLE STORES (
 STORE_ID	INT	NOT NULL,
 STORE_DESC	CHAR(72)	NOT NULL,
 ADDRESS1	CHAR(72)	NOT NULL,
 ADDRESS2	CHAR(72),
 CITY	CHAR(32)	NOT NULL,
 STATE	CHAR(2)	NOT NULL,
 POSTAL_CODE	CHAR(10)	NOT NULL,
 MANAGER_ID	INT	NOT NULL,
 PHONE	CHAR(32)	NOT NULL
);

ALTER TABLE STORES
ADD PRIMARY KEY (STORE_ID);

ALTER TABLE STORES
ADD FOREIGN KEY (MANAGER_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID);

CREATE TABLE ITEMS (
 ITEM_ID	INT	NOT NULL,
 ITEM_NAME	CHAR(72)	NOT NULL,
 ITEM_DESC	CHAR(72),
 PRICE	DECIMAL(10,2)	NOT NULL,
 WHOLESALE_COST	DECIMAL(10,2)	NOT NULL,
 DISCONTINUED_FLAG	INT,
 MANUFACTURER_ID	INT,
 DISTRIBUTOR_ID	INT
);

ALTER TABLE ITEMS
ADD PRIMARY KEY (ITEM_ID);

ALTER TABLE ITEMS
ADD FOREIGN KEY (MANUFACTURER_ID) REFERENCES MANUFACTURERS(MANUFACTURER_ID);

ALTER TABLE ITEMS
ADD FOREIGN KEY (DISTRIBUTOR_ID) REFERENCES DISTRIBUTORS(DISTRIBUTOR_ID);

CREATE TABLE PROMOTIONS (
 PROMOTION_ID	INT	NOT NULL,
 PROMOTION_NAME	CHAR(72)	NOT NULL,
 DESCRIPTION	CHAR(72),
 START_DATE	DATE	NOT NULL,
 END_DATE	DATE	NOT NULL
);

ALTER TABLE PROMOTIONS
ADD PRIMARY KEY (PROMOTION_ID);

CREATE TABLE ITEMS_IN_PROMOTIONS (
 PROMOTION_ID	INT	NOT NULL,
 ITEM_ID	INT	NOT NULL,
 COMMENTS	CHAR(72),
 DISCOUNT	DECIMAL(10,2)
);

ALTER TABLE ITEMS_IN_PROMOTIONS
ADD PRIMARY KEY (PROMOTION_ID, ITEM_ID);

ALTER TABLE ITEMS_IN_PROMOTIONS
ADD FOREIGN KEY (ITEM_ID) REFERENCES ITEMS(ITEM_ID);

ALTER TABLE ITEMS_IN_PROMOTIONS
ADD FOREIGN KEY (PROMOTION_ID) REFERENCES PROMOTIONS (PROMOTION_ID);

CREATE TABLE ORDERS (
 ORDER_ID	INT	NOT NULL,
 DATE_ENTERED	DATE	NOT NULL,
 DATE_PROMISED	DATE	,
 DATE_SHIPPED	DATE,
 EMPLOYEE_ID	INT,
 CUSTOMER_ID	INT,
 SALES_TAX_RATE	DECIMAL(5,4)	NOT NULL,
 STORE_ID	INT
)
;

ALTER TABLE ORDERS
ADD PRIMARY KEY(ORDER_ID);

ALTER TABLE ORDERS
ADD FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID);

ALTER TABLE ORDERS
ADD FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID);

ALTER TABLE ORDERS
ADD FOREIGN KEY (STORE_ID) REFERENCES STORES(STORE_ID);

CREATE TABLE ORDER_ITEMS (
 ORDER_ID	INT	NOT NULL,
 ITEM_ID	INT	NOT NULL,
 QUANTITY	INT	NOT NULL,
 DISCOUNT	DECIMAL(10,2)	NOT NULL
);


ALTER TABLE ORDER_ITEMS
ADD PRIMARY KEY(ORDER_ID, ITEM_ID);

ALTER TABLE ORDER_ITEMS
ADD FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID);

ALTER TABLE ORDER_ITEMS
ADD FOREIGN KEY (ITEM_ID) REFERENCES ITEMS(ITEM_ID);

COMMIT;

